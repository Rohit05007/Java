        Scanner sc = new Scanner(System.in);

        // Question (1)--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        for (int i = 4; i >= 1; i--) {// Number of rows
            for (int j = 1; j <= i; j++) {// Number of stars in the row
                System.out.print("* ");
            }
            System.out.println();
        }

        // Question (2)--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        System.out.print("Enter number :");
        int n = sc.nextInt();
        int sum = 0;
